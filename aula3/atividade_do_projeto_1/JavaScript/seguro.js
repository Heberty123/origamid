var font1_1 = document.querySelector(".font1-1");
var font2_1 = document.querySelector(".font2-1");
var img_font1 = document.querySelector(".imagem-font1");


font1_1.addEventListener('click', function(){
    font1_1.classList.toggle('semRadius');
    font2_1.classList.toggle('display-no');

    img_font1.classList.toggle('rotaciona');

});


